#!/usr/bin/env node
"use strict";

const crypto = require("node:crypto");
const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const sourcePath = path.join(root, "assets", "data", "menu.js");
const outputPath = path.join(root, "supabase", "seed.sql");

function sql(value) {
  if (value == null || value === "") return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}
function loadMenu() {
  const source = fs.readFileSync(sourcePath, "utf8");
  const context = { window: {} };
  vm.runInNewContext(source, context, { filename: sourcePath, timeout: 1000 });
  return {
    source,
    categories: context.window.POMMY_CATEGORIES,
    items: context.window.POMMY_MENU
  };
}

function validate(categories, items) {
  if (!Array.isArray(categories) || categories.length !== 10) {
    throw new Error(`Expected 10 canonical categories, found ${categories && categories.length}`);
  }
  if (!Array.isArray(items) || items.length !== 101) {
    throw new Error(`Expected exactly 101 canonical menu items, found ${items && items.length}`);
  }

  const categoryIds = new Set(categories.map((category) => category.id));
  const categoryNames = new Set(categories.map((category) => category.name));
  const slugs = new Set(items.map((item) => item.slug));
  const names = new Set(items.map((item) => item.name));

  if (categoryIds.size !== categories.length || categoryNames.size !== categories.length) {
    throw new Error("Canonical categories contain duplicate IDs or names");
  }
  if (slugs.size !== items.length || names.size !== items.length) {
    throw new Error("Canonical menu contains duplicate slugs or names");
  }

  items.forEach((item, index) => {
    if (item.id !== item.slug) throw new Error(`Item ${index + 1} has a noncanonical id/slug pair`);
    if (!categoryIds.has(item.category)) throw new Error(`Unknown category ${item.category} for ${item.slug}`);
    if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(item.slug)) throw new Error(`Invalid slug ${item.slug}`);
    if (!Number.isFinite(item.price) || item.price < 0) throw new Error(`Invalid price for ${item.slug}`);
    if (typeof item.available !== "boolean" || typeof item.featured !== "boolean") {
      throw new Error(`Invalid availability flags for ${item.slug}`);
    }
    if (!item.image || !item.name) throw new Error(`Missing required menu data for ${item.slug}`);
  });
}

function generate() {
  const { source, categories, items } = loadMenu();
  validate(categories, items);
  const sourceHash = crypto.createHash("sha256").update(source).digest("hex");

  const categoryRows = categories.map((category, index) =>
    `  (${sql(category.name)}, ${sql(category.id)}, null, ${index}, true)`
  ).join(",\n");

  const itemRows = items.map((item, index) =>
    `  (${sql(item.name)}, ${sql(item.slug)}, ${sql(item.category)}, ${sql(item.description)}, ${item.price.toFixed(2)}, ${sql(item.image)}, ${item.available}, ${item.featured}, ${index})`
  ).join(",\n");

  return `-- Generated by scripts/generate-supabase-seed.cjs from assets/data/menu.js.
-- Source SHA-256: ${sourceHash}
-- Do not edit this file manually.

begin;

insert into public.categories (name, slug, description, sort_order, is_active)
values
${categoryRows}
on conflict (slug) do update set
  name = excluded.name,
  description = excluded.description,
  sort_order = excluded.sort_order,
  is_active = excluded.is_active;

with seed_items (name, slug, category_slug, description, price, image_url, is_available, is_featured, sort_order) as (
  values
${itemRows}
)
insert into public.menu_items (
  name, slug, category_id, description, price, image_url, is_available, is_featured, sort_order
)
select
  seed.name, seed.slug, category.id, seed.description, seed.price, seed.image_url,
  seed.is_available, seed.is_featured, seed.sort_order
from seed_items seed
join public.categories category on category.slug = seed.category_slug
on conflict (slug) do update set
  name = excluded.name,
  category_id = excluded.category_id,
  description = excluded.description,
  price = excluded.price,
  image_url = excluded.image_url,
  is_available = excluded.is_available,
  is_featured = excluded.is_featured,
  sort_order = excluded.sort_order;

do $$
begin
  if (select count(*) from public.categories) <> 10 then
    raise exception 'Seed validation failed: expected exactly 10 categories';
  end if;
  if (select count(*) from public.menu_items) <> 101 then
    raise exception 'Seed validation failed: expected exactly 101 menu items';
  end if;
  if (select count(*) from public.menu_items where is_featured) <> 4 then
    raise exception 'Seed validation failed: expected exactly 4 canonical featured flags';
  end if;
end;
$$;

commit;
`;
}

const generated = generate();
if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8").replace(/\r\n/g, "\n")
    : "";
  if (current !== generated) {
    console.error("supabase/seed.sql is stale; run node scripts/generate-supabase-seed.cjs");
    process.exit(1);
  }
  console.log("Supabase seed is current: 10 categories, 101 items, 4 featured flags.");
} else {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated, "utf8");
  console.log("Generated supabase/seed.sql with 10 categories and exactly 101 menu items.");
}
